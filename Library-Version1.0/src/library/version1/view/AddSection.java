    private void setNewForm(){
        nameText.setText(null);
        addButton.setEnabled(false);
        try {
            idText.setText(IdGenarate.getId("S", "SId","Section"));
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
        nameText.requestFocus();
        
    }
    
    
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try {
            String id=idText.getText();
            String number=nameText.getText();
            Section section=new Section(id,number);
            int result = SectionController.addSection(section);        // TODO add your handling code here:
            if(result==1){
                JOptionPane.showMessageDialog(this, "Section Added");
            }
            setNewForm(); 
            if(close!=0){
               this.setVisible(false);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
           JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
       
        
    }//GEN-LAST:event_addButtonActionPerformed

