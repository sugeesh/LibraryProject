    public SPlashScreen(final int user) {
        initComponents();
        setLocationRelativeTo(null);

        progressBar.setBackground(Color.black);

        setResizable(false);

        final Timer timer = new Timer(300, null);
        timer.addActionListener(new ActionListener() {
            int value = 0;

            public void actionPerformed(ActionEvent e) {
                value += 2;
                progressBar.setValue(value);

                if (value < 10) {
                    progressLabel.setText("Connecting . . .");
                } else if (value < 30) {
                    progressLabel.setText("Analysing Database . . .");
                } else if (value < 40) {
                    try {
                        progressLabel.setText("Connecting Database . . .");
                        DBConnection.getDBConnection();
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(SPlashScreen.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (SQLException ex) {
                        Logger.getLogger(SPlashScreen.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (value < 60) {
                    progressLabel.setText("Analysing Database . . . ");
                } else if (value < 80) {
                    progressLabel.setText("Connected . . . ");
                } else if (value < 90) {
                    progressLabel.setText("Starting Library . . .");
                } else if (value < 99) {
                    timer.setDelay(1000);
                } else if (value == 100) {
                    dispose();
                    if (user == 0) {
                        new MainFrameNormal().setVisible(true);
                    } else if (user == 1) {
                        new MainFrameAdmin().setVisible(true);
                    }
                }
            }
        });

        timer.start();
    }

