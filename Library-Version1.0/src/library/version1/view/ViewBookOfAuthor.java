        private void setSearchTable(String id) throws ClassNotFoundException, SQLException{
            ArrayList<String> books = BookAuthorDetailController.searchByAuthorId(id);
            String name = AuthorController.getAuthorName(id);
            this.authorName=name;
            for(String accessionNumber : books){
                ArrayList<BookSearch> bookSearch = new ArrayList<BookSearch>();
                BookSearch book = new BookSearch(accessionNumber);
                bookSearch.add(book);
                bookSearch = BookTitleController.getBooksDetails(bookSearch);
                bookSearch = BookAuthorDetailController.getBooksDetails(bookSearch);
                bookSearch = BookController.getBookDetails(bookSearch);
                
                for(BookSearch realBook : bookSearch){
                    Object[] row = {realBook.getTitle(),realBook.getAccessionNumber(),realBook.getSectionName(),realBook.getCategoryName(),realBook.isAvailability()};
                    authorTableModel.addRow(row);
                }

            }


        }


