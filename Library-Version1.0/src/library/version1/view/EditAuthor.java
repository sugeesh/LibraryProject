    private void setForm(){
        try {
            idText.setText(authorId);
            nameText.setText(AuthorController.getAuthorName(authorId));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
        
        nameText.requestFocus();
    }
    
    
    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        String id=idText.getText();
        String name=nameText.getText();
        Author author =new Author(id,name);
        try {
            int result=AuthorController.updateAuthor(author);
            if(result==1){
                JOptionPane.showMessageDialog(this, "Author Updated", "Updated Message",JOptionPane.INFORMATION_MESSAGE);
            }
            this.setVisible(false);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_editButtonActionPerformed

