    private void logInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInButtonActionPerformed
        try {
                String userName = userNameText.getText();
                char[] ch = passwordText.getPassword();
                String password = new String(ch);

                User user = new User(0,userName, password);

                int privilege = UserController.searchUser(user);
                if(privilege == -1){
                    JOptionPane.showMessageDialog(this, "Invalied User Name Or Password");
                }else if(privilege == 0){
                    new SPlashScreen(0).setVisible(true);
                    this.setVisible(false);
                }else if(privilege == 1){
                     new SPlashScreen(1).setVisible(true);
                     this.setVisible(false);
                 }
                    
            // TODO add your handling code here:
        }catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_logInButtonActionPerformed

