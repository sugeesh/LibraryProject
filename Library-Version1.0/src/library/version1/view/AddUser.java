    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            String userName = userNameText.getText();
            char[] ch = passwordText.getPassword();
            String password = new String(ch);

            String id = IdGenarate.getId("U", "UId", "User");
            
            User user = new User(id,userName, password, this.privilege);
                // TODO add your handling code here:
            int result = UserController.addUser(user);

           if(result == 1){
                JOptionPane.showMessageDialog(this, "User Added");
                this.setVisible(false);
           }else{
                JOptionPane.showMessageDialog(this, "User Added Failed");
           }                            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }                        // TODO add your handling code here:
    }//GEN-LAST:event_saveButtonActionPerformed

