    private void setForm(String id){
        try {
            this.id = id;
            User user = UserController.getUserDetail(id);
            userNameText.setText(user.getUserName());
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
        
    
    }
    
    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try {
            String userName = userNameText.getText();
            char[] ch = passwordText.getPassword();
            String password = new String(ch);

            String id = this.id;
            
            User user = new User(id,userName, password);
                // TODO add your handling code here:
            int result = UserController.updateUser(user);

           if(result == 1){
                JOptionPane.showMessageDialog(this, "User Updated");
                this.setVisible(false);
           }else{
                JOptionPane.showMessageDialog(this, "User Update Failed");
           }                            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }       // TODO add your handling code here:
    }//GEN-LAST:event_updateButtonActionPerformed

