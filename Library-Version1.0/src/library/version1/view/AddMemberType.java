    private void setNewForm(){
        typeText.setText(null);
        addButton.setEnabled(false);
        try {
            idText.setText(IdGenarate.getId("MT", "MTId","MemberType"));
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
        typeText.requestFocus();
        
    }
    
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String id= idText.getText(); 
        String type=typeText.getText();
        MemberType memberType=new MemberType(id,type);
        try {
            int result =MemberTypeController.addMemberType(memberType);
            if(result==1){
                JOptionPane.showMessageDialog(this, "New Memeber Type Added");
            }
            
            setNewForm(); 
            if(close!=0){
               this.setVisible(false);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_addButtonActionPerformed

