    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if(ordersTable.getSelectedRow()!=-1){
            int selectedRow= ordersTable.getSelectedRow();
            String oId=orderTableModel.getValueAt(selectedRow,0).toString();

            int option =JOptionPane.showConfirmDialog(this, "Are you sure to this Order ?", "Delete Order", JOptionPane.YES_NO_OPTION);        // TODO add your handling code here:
            if(option==JOptionPane.YES_OPTION){
                    try {
                        int result =OrderController.deleteOrder(oId);
                        if(result==1){
                            JOptionPane.showMessageDialog(this, "Order Deleted ");
                            setTable();
                        }
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
                    } catch (ClassNotFoundException ex) {
                        JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
                    }
             }
       }else{
            JOptionPane.showMessageDialog(this, "Select Order First");
        }    
        // TODO add your handling code here:
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void setTable(){
        try {
            int rowCount=orderTableModel.getRowCount();
            for(int i=0;i<rowCount;i++){
                orderTableModel.removeRow(0);
            }          
            
            ArrayList<OrderDetail> orderDetails = OrderController.getAllOrderDetail();
           
            for(OrderDetail orderDetail : orderDetails){
                String memberName = MemberController.getMemberName(orderDetail.getMemberNumber());
                Object[] row ={orderDetail.getoId(),orderDetail.getAccessionNumber(),orderDetail.getTitle(),orderDetail.getMemberNumber(),memberName,orderDetail.isReturned()};
                orderTableModel.addRow(row);
            }
            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Database Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Connection Error ","Error Message", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    
    
    
    
    
